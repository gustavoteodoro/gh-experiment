export function oswaldRegular(fontSize) {
    return `
        font-family: 'Oswald', sans-serif;
        font-size: ${fontSize};
        font-weight: 400;
        -webkit-font-smoothing: antialiased;
        -moz-osx-font-smoothing: grayscale;
    `;
}

